func (r *DummyRepository) GetResources(resourceType ResourceType) ([]any, error) {
	switch resourceType {
	case ResourceTypePod:
		pods, err := r.GetPods()
		if err != nil {
			return nil, err
		}
		result := make([]any, len(pods))
		for i, p := range pods {
			result[i] = p
		}
		return result, nil
	case ResourceTypeDeployment:
		deployments, err := r.GetDeployments()
		if err != nil {
			return nil, err
		}
		result := make([]any, len(deployments))
		for i, d := range deployments {
			result[i] = d
		}
		return result, nil
	case ResourceTypeService:
		services, err := r.GetServices()
		if err != nil {
			return nil, err
		}
		result := make([]any, len(services))
		for i, s := range services {
			result[i] = s
		}
		return result, nil
	default:
		return []any{}, nil
	}
}

